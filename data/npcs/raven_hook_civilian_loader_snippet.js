function preloadCivilianSprites(scene) {
  for (const npc of RAVEN_HOOK_CIVILIAN_MANIFEST) {
    scene.load.spritesheet(npc.spriteKey, npc.path, { frameWidth: npc.frameWidth, frameHeight: npc.frameHeight });
  }
}

function createCivilianWalkAnims(scene) {
  const dirs = ['S','SW','W','NW','N','NE','E','SE'];
  for (const npc of RAVEN_HOOK_CIVILIAN_MANIFEST) {
    for (let row = 0; row < dirs.length; row++) {
      const key = `${npc.spriteKey}_${dirs[row]}`;
      if (scene.anims.exists(key)) continue;
      scene.anims.create({ key, frames: scene.anims.generateFrameNumbers(npc.spriteKey, { start: row*4, end: row*4+3 }), frameRate: 6, repeat: -1 });
    }
  }
}
